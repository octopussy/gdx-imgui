package com.borschlab.gdx.imgui;

/**
 * @author octopussy
 */
public class ImVec2 {
  public float x;
  public float y;

  public float getX() {
    return x;
  }

  public void setX(float v) {
    x = v;
  }

  public float getY() {
    return y;
  }

  public void setY(float v) {
    y = v;
  }
}
